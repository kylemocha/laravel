                    <p style="font-size: 18px;">
                        Whilst the pandemic has, and continues to, take its toll on our mental health, the ability to reconnect through World Mental Health Day 2022 will provide us with an opportunity to re-kindle our efforts to protect and improve mental health.
                        
                        Many aspects of mental health have been challenged; and already before the pandemic in 2019 an estimated one in eight people globally were living with a mental disorder. At the same time, the services, skills and funding available for mental health remain in short supply, and fall far below what is needed, especially in low and middle income countries.<br>
                        <br>
                        The COVID-19 pandemic has created a global crisis for mental health, fueling short- and long-term stresses and undermining the mental health of millions. Estimates put the rise in both anxiety and depressive disorders at more than 25% during the first year of the pandemic. 
                        At the same time, mental health services have been severely disrupted and the treatment gap for mental health conditions has widened. <br>
                        <br>
                        Growing social and economic inequalities, protracted conflicts, violence and public health emergencies affect whole populations, threatening progress towards improved well-being; a staggering 84 million people worldwide were forcibly displaced during 2021.  We must deepen the value and commitment we give to mental health as individuals, communities and governments and match that value with more commitment, engagement and investment by all stakeholders, across all sectors.  We must strengthen mental health care so that the full spectrum of mental health needs is met through a community-based network of accessible, affordable and quality services and supports. <br>
                        <br>
                        Stigma and discrimination continue to be a barrier to social inclusion and access to the right care; importantly, we can all play our part in increasing awareness about which preventive mental health interventions work and World Mental Health Day is an opportunity to do that collectively. We envision a world in which mental health is valued, promoted and protected; where everyone has an equal opportunity to enjoy mental health and to exercise their human rights; and where everyone can access the mental health care they need. <br>
                        <br>
                        WHO will work with partners to launch a campaign around the theme of Making Mental Health & Well-Being for All a Global Priority. This will be an opportunity for people with mental health conditions, advocates, governments, employers, employees and other stakeholders to come together to recognize progress in this field and to be vocal about what we need to do to ensure Mental Health & Well-Being becomes a Global Priority for all.
